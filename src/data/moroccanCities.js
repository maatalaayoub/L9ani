// Function to get cities formatted for dropdown based on locale
export const getCitiesForDropdown = (locale = 'en') => {
  return moroccanCities.map(city => ({
    value: city.value,
    label: locale === 'ar' ? city.labelAr : city.labelEn
  })).sort((a, b) => a.label.localeCompare(b.label, locale === 'ar' ? 'ar' : 'en'));
};

// Function to get city label by value
export const getCityLabel = (value, locale = 'en') => {
  const city = moroccanCities.find(c => c.value === value);
  if (!city) return value; // Return the value itself if it's a custom city
  return locale === 'ar' ? city.labelAr : city.labelEn;
};

